import { mongoose } from "../config/database.js";
const { Schema } = mongoose;


const AiExtractionSchema = new Schema({
  // Referência à página raspada de origem
  scrapedPageId: {
    type: Schema.Types.ObjectId,
    ref: "ScrapedPage",
    required: true,
  },

  // Referência ao curso de origem
  courseId: {
    type: Schema.Types.ObjectId,
    ref: "Course",
    required: false,
  },

  // Semantic data Extracted by AI
  extractedData: {
    courseName: { type: String, required: true },
    institution: { type: String, required: true },
    targetAudience: { type: String }, // Target audience requests
    registrationLink: { type: String }, // Registration link
    deadline: { type: String }, // Specific deadline found
    isFree: { type: Boolean, required: true },
    isEaD: { type: Boolean, required: true },
  },

  // Post Draft generated by AI ???
  suggestedPostText: {
    type: String,
  },

  // Flow control of curation for Administrator
  curationStatus: {
    type: String,
    enum: [
      "PENDING_REVIEW",
      "APPROVED_FOR_POSTING",
      "REJECTED_ALUCINATION",
      "REJECTED_NOT_RELEVANT",
    ],
    default: "PENDING_REVIEW",
  },

  // Extraction Date
  processedAt: {
    type: Date,
    default: Date.now,
  },

  // History of WHO approved
  reviewedBy: {
    type: String,
    required: false,
  },
});

export const AiExtraction = mongoose.model("AiExtraction", AiExtractionSchema);
